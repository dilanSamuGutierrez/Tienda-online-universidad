    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = nombreCategoria.getText();
        
        boolean nuevaCategoria = this.controller.Create(nombre);
        
        if(nuevaCategoria){
        JOptionPane.showMessageDialog(this, "Se creo exitosamente la nueva categoria");
        nombreCategoria.setText("");
        }else{
            JOptionPane.showMessageDialog(this, "Hubo un error al crear la nueva categoria");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

