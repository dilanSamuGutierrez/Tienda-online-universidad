    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        int id= Integer.parseInt(idCategoriaBuscar.getText());
        
        Categoria buscarCategoria=this.controller.Read(id);
        
        if(buscarCategoria != null){
        tituloResultado.setVisible(true);
        resultadoBusqueda.setText(buscarCategoria.getNombre());
        }else{
            resultadoBusqueda.setText("No se encontraron resultados");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

