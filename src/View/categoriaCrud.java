    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String busqueda = nombreCategoriaBuscar.getText().trim();

        List<Categoria> buscarCategoria = this.controller.Read(busqueda.toLowerCase());

        DefaultListModel<Categoria> modelo = (DefaultListModel<Categoria>) resultadosBusqueda.getModel();
        

        modelo.removeAllElements();

        if (busqueda.isEmpty()) {
            jScrollPane2.setVisible(false);
            resultadosBusqueda.setVisible(false);
            modelo.removeAllElements();
            tituloResultado.setText("Por favor llene el campo de busqueda");
            return;
        }
        if (buscarCategoria == null || buscarCategoria.isEmpty()) {
            jScrollPane2.setVisible(false);
            resultadosBusqueda.setVisible(false);
            modelo.removeAllElements();
            tituloResultado.setText("No se encontraron resultados");
            return;
        }

        tituloResultado.setText("Resultado");
        jScrollPane2.setVisible(true);
        resultadosBusqueda.setVisible(true);

        for (Categoria cat : buscarCategoria) {
            modelo.addElement(cat);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

