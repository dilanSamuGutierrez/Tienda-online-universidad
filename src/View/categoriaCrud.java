    public void eventoClickEnLista(MouseEvent e) {
        if (e.getClickCount() == 2) {
            int index = resultadosBusqueda.locationToIndex(e.getPoint());
            if (index >= 0) {
                Categoria categoriaSeleccionada =(Categoria) resultadosBusqueda.getModel().getElementAt(index);
                int id= categoriaSeleccionada.getId();
                String nombre=categoriaSeleccionada.getNombre();
                String[] opciones = {"Editar", "Borrar"};
                
                int respuesta = JOptionPane.showOptionDialog(this, "Que acción desea hacer con la categoria: " + nombre, nombre, JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, opciones, EXIT_ON_CLOSE);
                
                if (respuesta == 0) {
                    String respuestaEditar = JOptionPane.showInputDialog(this,"Editar categoria: ");
                    if(respuestaEditar == null){
                    return; }
                    this.controller.Edit(id, respuestaEditar);
                    JOptionPane.showMessageDialog(this, "Edición realizada con exito");
                    jButton2ActionPerformed(null);
                } else if (respuesta == 1) {
                    int respuestaBorrar = JOptionPane.showConfirmDialog(this, "¿Esta seguro que desea borrar esta categoria?");
                    if (respuestaBorrar == JOptionPane.YES_OPTION) {
                        this.controller.Delete(id);
                        JOptionPane.showMessageDialog(this, "Se ha borrado la categoria");
                        jButton2ActionPerformed(null);
                    }
                }
            }
        }
    }

